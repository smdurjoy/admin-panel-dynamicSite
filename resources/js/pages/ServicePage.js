import React, {Component, Fragment} from 'react';
import MainLayout from "../components/MainLayout";
import Axios from "axios";
import Swal from "sweetalert2";
import {Button, Col, Container, Row} from "react-bootstrap";
import Loading from "../components/Loading";
import WentWrong from "../components/WentWrong";
import BootstrapTable from "react-bootstrap-table-next";
import paginationFactory from "react-bootstrap-table2-paginator";
import {Link} from "react-router-dom";
import {MDBBtn, MDBContainer, MDBInput, MDBModal, MDBModalBody, MDBModalFooter, MDBModalHeader} from "mdbreact";

class ServicePage extends Component {
    constructor() {
        super();
        this.state = {
            dataList: [],
            isLoading: true,
            isError: false,
            selectRowId: '',
            deleteBtnText: 'Delete',
            addModal: false,
            service_name: '',
            service_description: '',
            service_image: '',
            editModal: false
        }

        this.deleteRow = this.deleteRow.bind(this)
        this.imgCellFormat = this.imgCellFormat.bind(this)
        this.addModalToggle = this.addModalToggle.bind(this)
        this.addServiceButtonClick = this.addServiceButtonClick.bind(this)
        this.addService = this.addService.bind(this)
        this.editRow = this.editRow.bind(this)
        this.editService = this.editService.bind(this)
        this.editModalToggle = this.editModalToggle.bind(this)
    }

    componentDidMount() {
        Axios.get('/serviceData').then((response) => {
            if(response.status == 200) {
                this.setState({dataList: response.data, isLoading: false})
            } else {
                this.setState({isLoading: false, isError: true})
            }
        }).catch((error) => {
            this.setState({isLoading: false, isError: true})
        })
    }

    deleteRow() {
        if(this.state.selectRowId == '') {
            return Swal.fire('Please select a row for delete!')
        } else {
            Swal.fire({
                title: 'Are you sure?',
                text: "You won't be able to revert this!",
                icon: 'warning',
                showCancelButton: true,
                confirmButtonColor: '#3085d6',
                cancelButtonColor: '#d33',
                confirmButtonText: 'Yes, delete it!'
            }).then((result) => {
                if (result.value) {
                    this.setState({
                        deleteBtnText: "Deleting..."
                    })
                    Axios.post('/serviceDelete', {id:this.state.selectRowId}).then((response) => {
                        if(response.data == 1 && response.status == 200) {
                            this.setState({deleteBtnText: "Delete Success"})
                            setTimeout(function () {
                                this.setState({deleteBtnText: "Delete"})
                            }.bind(this), 2000)
                            this.componentDidMount()
                        } else {
                            this.setState({deleteBtnText: "Delete Failed"})
                        }
                    }).catch((error) => {
                        this.setState({deleteBtnText: "Delete Failed"})
                    })
                }
            });
        }
    }

    imgCellFormat(cell) {
        return <img className="tableImage" src={cell}/>
    }

    // Add service functions
    addModalToggle() {
        if(this.state.addModal == false) {
            this.setState({addModal: true});
        } else {
            this.setState({
                addModal: false
            });
        }
    }

    addServiceButtonClick() {
        let serviceName = document.getElementById('serviceName').value;
        let serviceDes = document.getElementById('serviceDes').value;
        let serviceImg = document.getElementById('serviceImg').value;

        this.addService(serviceName, serviceDes, serviceImg)
    }

    addService(serviceName, serviceDes, serviceImg) {
        Axios.post('/addService', {
            service_name: serviceName,
            service_description: serviceDes,
            service_image: serviceImg
        }).then((response) => {
            if(response.status == 200 && response.data == 1) {
                Swal.fire('Service Added Successfully !');
                this.addModalToggle();
                this.componentDidMount();
            } else {
                Swal.fire('Something Went Wrong !');
                this.addModalToggle();
            }
        }).catch((error) => {
            Swal.fire('Something Went Wrong !');
            this.addModalToggle();
        })
    }

    // Edit project functions
    editRow() {
        const id = this.state.selectRowId
        const serviceName = this.state.service_name
        const serviceDes = this.state.service_description
        const serviceImg = this.state.service_image

        this.editService(id, serviceName, serviceDes, serviceImg)
    }

    editService(id, serviceName, serviceDes, serviceImg) {
        Axios.post('/editService', {
            id: id,
            service_name: serviceName,
            service_description: serviceDes,
            service_image: serviceImg,
        }).then((response) => {
            if(response.status == 200) {
                Swal.fire('Service has been updated !')
                this.editModalToggle()
                this.componentDidMount()
            } else {
                Swal.fire('Something Went Wrong !')
                this.editModalToggle()
            }
        }).catch((error) => {
            Swal.fire('Something Went Wrong !')
            this.editModalToggle()
        })
    }

    editModalToggle() {
        if(this.state.selectRowId == '') {
            return Swal.fire('Please select a row for Update!')
        } else {
            if(this.state.editModal == false) {
                this.setState({editModal: true});
                Axios.post('/serviceEditDetails', {id:this.state.selectRowId}).then((response) => {
                    if(response.status == 200) {
                        this.setState({
                            service_name: response.data[0]['service_name'],
                            service_description: response.data[0]['service_description'],
                            service_image: response.data[0]['service_image']
                        });
                    } else {
                        Swal.fire('Something Went Wrong !');
                    }
                }).catch((error) => {
                    Swal.fire('Something Went Wrong !');
                })
            } else {
                this.setState({
                    editModal: false
                });
            }
        }
    }

    render() {
        if(this.state.isLoading == true) {
            return(
                <MainLayout title="Service">
                    <Container>
                        <Loading/>
                    </Container>
                </MainLayout>
            )
        } else if(this.state.isError == true) {
            return(
                <MainLayout title="Service">
                    <Container>
                        <WentWrong/>
                    </Container>
                </MainLayout>
            )
        } else {
            const data = this.state.dataList;
            const columns = [
                {dataField: "id", text: "ID"},
                {dataField: "service_image", text: "Image", formatter:this.imgCellFormat},
                {dataField: "service_name", text: "Name"},
                {dataField: "service_description", text: "Description"},
            ]

            const selectRow = {
                mode: "radio",
                onSelect: (row, isSelect, rowIndex) => {
                    this.setState({selectRowId: row['id']})
                }
            }

            return (
                <Fragment>
                    <MainLayout title="Service">
                        <Container>
                            <Row>
                                <Col md={4} lg={4} sm={4}>
                                    <Button className="btn btn-info mt-5 btn-sm" onClick={this.addModalToggle}>Add</Button>
                                    <Button className="btn btn-info mt-5 btn-sm" onClick={this.editModalToggle}>Edit</Button>
                                    <Button className="btn btn-danger mt-5 btn-sm" onClick={this.deleteRow}>{ this.state.deleteBtnText }</Button>
                                </Col>
                                <Col md={4} lg={4} sm={4}>
                                    <h1 className=" text-center titleText mt-5">Services</h1>
                                </Col>
                                <Col md={4} lg={4} sm={4}>
                                    <h1 className="desText float-right mt-5">
                                        <Link to='/' className="pageLink">Home</Link> / <Link to='/services' className="pageLink">Services</Link>
                                    </h1>
                                </Col>
                            </Row>
                            <Row className="mt-3">
                                <Col lg={12} md={12} sm={12}>
                                    <BootstrapTable
                                        keyField='id'
                                        data={ data }
                                        columns={ columns }
                                        pagination={ paginationFactory() }
                                        selectRow={selectRow}
                                    />
                                </Col>
                            </Row>
                        </Container>

                        {/*service add modal*/}
                        <MDBContainer>
                            <MDBModal isOpen={this.state.addModal} toggle={this.addModalToggle} size="lg">
                                <MDBModalHeader toggle={this.addModalToggle}>Add Project</MDBModalHeader>
                                <MDBModalBody>
                                    <Row>
                                        <Col md={6} sm={6} lg={6}>
                                            <MDBInput label="Service Name" icon="envelope" group type="text" validate error="wrong"
                                                      success="right" id="serviceName"/>
                                            <MDBInput label="Service Image" icon="envelope" group type="text" validate error="wrong"
                                                      success="right" id="serviceImg"/>
                                        </Col>
                                        <Col md={6} sm={6} lg={6}>
                                            <MDBInput type="textarea" label="Service Description" rows="3" id="serviceDes"/>
                                        </Col>
                                    </Row>
                                </MDBModalBody>
                                <MDBModalFooter>
                                    <MDBBtn color="primary" size="sm" onClick={this.addServiceButtonClick}>Add</MDBBtn>
                                    <MDBBtn color="danger" size="sm" onClick={this.addModalToggle}>Close</MDBBtn>
                                </MDBModalFooter>
                            </MDBModal>
                        </MDBContainer>

                        {/*project edit modal*/}
                        <MDBContainer>
                            <MDBModal isOpen={this.state.editModal} toggle={this.editModalToggle} size="lg">
                                <MDBModalHeader toggle={this.editModalToggle}>Edit Course</MDBModalHeader>
                                <MDBModalBody>
                                    <Row>
                                        <Col md={6} sm={6} lg={6}>
                                            <MDBInput label="Service Name" icon="envelope" group type="text" validate error="wrong"
                                                      success="right" value={ this.state.service_name } onChange={e => this.setState({ service_name: e.target.value })}/>
                                            <MDBInput label="Service Image" icon="envelope" group type="text" validate error="wrong"
                                                      success="right" value={ this.state.service_image } onChange={e => this.setState({ service_image: e.target.value })}/>
                                        </Col>
                                        <Col md={6} sm={6} lg={6}>
                                            <MDBInput type="textarea" label="Service Description" rows="4" value={ this.state.service_description } onChange={e => this.setState({ service_description: e.target.value })}/>
                                        </Col>
                                    </Row>
                                </MDBModalBody>
                                <MDBModalFooter>
                                    <MDBBtn color="primary" size="sm" onClick={this.editRow}>Update</MDBBtn>
                                    <MDBBtn color="danger" size="sm" onClick={this.editModalToggle}>Close</MDBBtn>
                                </MDBModalFooter>
                            </MDBModal>
                        </MDBContainer>

                    </MainLayout>
                </Fragment>
            );
        }
    }
}

export default ServicePage;
